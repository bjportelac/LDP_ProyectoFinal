public class GraphicaLibraries {

    private Boolean Basic = Boolean.TRUE;
    private Boolean Objects = Boolean.FALSE;

    public GraphicaLibraries() {
    }

    public Boolean getBasic() {
        return Basic;
    }

    public void setBasic(Boolean basic) {
        Basic = basic;
    }

    public Boolean getObjects() {
        return Objects;
    }

    public void setObjects(Boolean objects) {
        Objects = objects;
    }
}
